    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno=jTextField2.getText();
            try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","december24");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            if(rs.next()){
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(1));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField10.setText(rs.getString(6));
                jTextField6.setText(rs.getString(7));
                jTextField7.setText(rs.getString(8));
                jTextField8.setText(rs.getString(9));
                jTextField11.setText(rs.getString(10));
                
                int m1=Integer.parseInt(jTextField10.getText());
                int m2=Integer.parseInt(jTextField11.getText());
                int m3=Integer.parseInt(jTextField7.getText());
                int m4=Integer.parseInt(jTextField8.getText());
                int m5=Integer.parseInt(jTextField6.getText());
                
                String totalmarks=String.valueOf(m1+m2+m3+m4+m5);
                jTextField9.setText(totalmarks);
                    
                
            }
            }
         
             catch(Exception e){
             JOptionPane.showMessageDialog(null, e.toString());
             }
    }//GEN-LAST:event_formComponentShown

